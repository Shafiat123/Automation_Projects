package Day_05;

public class T2_if_else {
    public static void main(String[] args) {

        //describe two integer numbers
        int number1 = 100;
        int number2 = 200;

        //using if condition print only when a is less than b
        if (number1 < number2) {
            System.out.println("Number1 is less than Number2");
        } else {
            System.out.println("Number1 is not less than Number2");
        }
    }//end of main
}//end of class
